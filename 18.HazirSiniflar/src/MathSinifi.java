public class MathSinifi {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100)); // 100
        System.out.println(Math.cbrt(8)); // 2
        System.out.println(Math.ceil(3.2)); // 4
        System.out.println(Math.floor(3.3)); // 3
        System.out.println(Math.max(20, 30)); // 30
        System.out.println(Math.min(20, 30)); //20
        System.out.println(Math.pow(2,3)); // 8
        System.out.println(Math.random() * 100);
        System.out.println(Math.round(10.3)); // 10
        System.out.println(Math.sqrt(4)); // 2

    }
}
/*
 * Adında da anlaşılacağı gibi Java'da Math sınıfı matematiksel fonksiyonlar sunmaktadır. 
 * Math.MethodAdi(Parametreler) şeklinde çalışır.
 * Math.abs(x) => x'in mutlak değerini verir.
 * Math.cbrt(x) => x'in küpkökünü verir.
 * Math.ceil(x) => x'i en yakın tamsayıya yuvarlar. Bir yukarıya yuvarlar.
 * Math.floor(x) => x'i aşağı yuvarlar.
 * Math.min(x,y) => En düşük değere sahip sayıyı verir.
 * Math.max(x,y) => En büyük sayıyı verir.
 * Math.pow(x,y) => üs almayı sağlar
 * Math.random() => 0 ile 1 arasında rasgele sayı üretir.
 * Math.round(x) => Değer .5'ten büyükse yukarı değilse aşağı yuvarlar.
 * Math.sqrt(x) => x'in karekökünü verir. 
 */