public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }
        System.out.println("--------------------");
        
    }
}
/**
 * For Döngüsü
 * for (durum 1; durum 2; durum 3) {
 * // çalışacak kod bloğu
 * }
 * Durum 1: Döngü başlangıcındaki değer ataması
 * Durum 2: Döngünün çalışacağı koşul
 * Durum 3: Döngü her çalıştığında arttırılacak değer
 * Tekrar sayısı belli olan döngülerde kullanılır.
 */