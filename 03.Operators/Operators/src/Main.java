public class Main {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        int c = 6;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("a + b + c = " + (a + b + c));
        System.out.println("a + b - c = " + (a + b - c));
    }
}

